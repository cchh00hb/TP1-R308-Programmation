class Point:
    def __init__(self, x=0.0, y=0.0):
        self.__x = x
        self.__y = y

def __repr__(self):
        return f"Point({self.x}, {self.y})"




class Rectangle:
    def surface(self):
        return self.longueur * self.hauteur

    def perimetre(self):
        return 2 * (self.longueur + self.hauteur)
